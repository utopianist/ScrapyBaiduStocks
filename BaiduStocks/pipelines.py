class BaidustocksPipeline(object):
    def open_item(self, spider):
        self.f = open('baiduStockInfo.txt', 'w')

    def close_item(self, spider):
        self.f.close()

    def process_item(self, item, spider):
        try:
            line = str(dict(item)) + '\n'
            self.f.write(line)
        except:
            pass
        return item
